int	ft_countwords(char const *s, char c)
{
	int				words;
	unsigned int	i;

	i = 0;
	words = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c && ((i > 0) ? s[i - 1] == c : 1))
			words++;
		i++;
	}
	return (words);
}
